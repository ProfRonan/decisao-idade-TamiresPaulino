numero = int(input('Digite um número: '))

if numero < 0:
    print('impossível!')
    #print('não precisa se alistar')

if numero < 18:
    print('não precisa se alistar.')

elif 18 < numero < 65:
    print('Não esqueça de votar na próxima eleição.')

elif numero > 65:
    print('Vá descansar.')
    
else: 
    print('eita!')







        
